/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *detectCycle(ListNode *head) {
      ListNode *slow, *fast;

      if(!head || !head->next){
        return NULL;
      }
      fast=head;
      slow=head;

      while(fast && fast->next){
        cout<<"slow:"<<slow->val<<" fast:"<<fast->val<<"\n";
        slow = slow->next;
        fast = fast->next->next;
        if(slow==fast){
          cout<<"match A:"<<fast->val<<"\n";
          break;
        }
      }

      if(fast==slow){
        fast=head;
        while(fast!=slow){
          fast=fast->next;
          slow=slow->next;
        }
        cout<<"match B:"<<fast->val<<"\n";
        return fast;
      }

      return NULL;
    }
};
